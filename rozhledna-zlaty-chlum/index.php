<h1>Rozhledna Zlatý chlum</h1>



<h2>Kamenná rozhledna Zlatý Chlum</h2>
<p>
Kdo zavítá do městečka Jeseník, neměl by si nechat ujít výhled na přilehlé okolí z rozhledny Zlatý Chlum. Tato rozhledna stojí na stejnojmenném vrchu s nadmořskou výškou 875 metrů. Kamenná rozhledna měří 26 metrů a sahá do výšky 901 m n. m. Na vrchol vyhlídkové plošiny se turisté dostanou po 145 schodech.
</p>

<h2>Kam až oko dohlédne?</h2>
<p>
Z rozhledny Zlatý Chlum se otevírá nádherný výhled na blízké město Jeseník, Otmuchovské jezero v Oderské nížině v Polsku a Hrubé Jeseníky s dominantními vrcholy Praděd a Šerák. Některým výletníkům se dokonce poštěstí za dobré viditelnosti spatřit Králický Sněžník a Rychlebské hory. 
</p>

<h2>Otevření rozhledny v roce 1899</h2>
<p>
Rozhledna stojí na vrchu Zlatý Chlum již od 19. století. O stavbu usiloval turistický spolek z Frývaldovské pobočky. Stavba rozhledny byla započata 8. 8. 1898 a jejími zakladateli se stal například zeť slavného léčitele Vinzence Priessnitze. Rozhledna se dočkala svého dokončení již 3. 9. 1899. Za necelé tři roky od zpřístupnění rozhledny byl zahájen provoz turistické chaty, což umožnilo návštěvy rozhledny i v zimním čase. 
</p>

<h2>Sáňkařská dráha a chata se zázemím</h2>
<p>
V roce 1932 vybudovala Frývaldovská pobočka trasu k turistické chatě a pro všechny milovníky zimy sáňkařskou dráhu. Pokud výletníci dostali hlad, mohli se občerstvit v hostinci Harichstein (Čertovy kameny) na počátku cesty k vyhlídce. Ve válečném a poválečném období rozhledna i chata utrpěly šrámy na svém vzhledu. Rozhledna se dočkala své obnovy po roce 1976, kdy přešla do správy města. O několik let později došlo také na rekonstrukci chaty.
</p>

<h2>Zajímavá fakta o rozhledně</h2>
<p class="list-paragraph">
K rozhledně Zlatý Chlum se váže řada zajímavostí:
</p>
<ul class="article-list">
<li>Rozhlednu turisté zahlédnou již z města Jeseník.</li>
<li>U rozhledny dodnes stojí chata s občerstvením a příjemným venkovním posezením.</li>
<li>Uvnitř rozhledny se nachází 110 dřevěných a 14 kovových schodů, po nichž se turisté dostanou na plošinu s okny. Po dalších 22 schodech vystoupají na její vrchol.</li>
<li>Rozhlednu postavil Alois Nitzche podle návrhu Eduarda Zelenky. </li>
<li>Rozhledna prošla v letech 2011 až 2012 rekonstrukcí za přispění peněz z fondů EU.</li>
</ul>

<h2>Otevírací doba rozhledny a vstupné</h2>
<p>
V současné době je rozhledna Zlatý Chlum otevřena od května do září, za příznivého počasí každý den. Návštěvníci se mohou kochat výhledem z rozhledny od 10 do 18 hodin. V zimním období je rozhledna zpřístupněna pouze o víkendech od 10 do 16 hodin, ale chata je v provozu po celý rok. Za vstup na rozhlednu zaplatí dospělí 25 Kč, studenti a důchodci 20 Kč a děti 15 Kč.
</p>

<h2>Kudy na rozhlednu Zlatý Chlum?</h2>
<p>
Na rozhlednu se turisté dostanou několika cestami. Vyrazit mohou z Jeseníku po červené či modré turistické značce (4,5 km s převýšením 450 m). Oblíbeným východiskem je také Křížový vrch, kam mohou turisté vyjet autem, zaparkovat a odtud se vydat po 2,5 km dlouhé trase s malým převýšením. Nejkratší cesta vede z parkoviště u chaty Čertovy kameny (1,5 km po modré značce).
</p>
