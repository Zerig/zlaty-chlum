<h1>Obec Česká Ves</h1>



<h2>Úchvatné okolí České Vsi</h2>
<p>
Obec Česká Ves je oblíbeným turistickým cílem, neboť je skvělým výchozím bodem k poznávání oblasti České vysočiny, pohoří Hrubý Jeseník a pohraničního Polska. Turisté se mohou ubytovat v kvalitním hotelu Zlatý Chlum a navštívit Aqua Centrum s krytým plaveckým bazénem, hřištěm na plážovou odbíjenou a restaurací.
</p>

<h2>Poloha obce Česká Ves</h2>
<p>
Obec Česká Ves leží ve Slezsku, pouze 3 km od města Jeseník a zhruba 73 km od města Olomouc. V obci žije necelých 2 400 stálých obyvatel. Česká Ves se rozprostírá podél řeky Bělé a sousedí s obcemi Písečná, Supíkovice, Stará Červená Voda, Lipová-lázně, Mikulovice a Jeseníky. Významným vrchem v blízkosti obce je Zlatý Chlum (891 m n. m.) s kamennou rozhlednou.
</p>

<h2>Zmínka o obci z 15. století</h2>
<p>
První dochovaná písemná zmínka o České Vsi pochází z roku 1416, kdy ji vlastnil frývaldovský rychtář Hynek Mušín z Hohenštejnu. Českou Ves negativně poznamenala třicetiletá válka (1618 – 1648) a morová epidemie z roku 1627. V druhé polovině 17. století se obci nevyhnuly čarodějnické procesy a smrt upálením postihla 16 žen a 1 dítě.
</p>

<h2>Nedávné a novodobé dějiny obce</h2>
<p>
Česká Ves se stala samostatnou obcí v roce 1850. Obec se dočkala svého rozvoje v roce 1894, kdy v ní byla založena továrna na výrobu železného zboží, která funguje dodnes. Česká Ves byla od roku 1976 do roku 1990 součástí města Jeseník. V dnešní době je obec členem Mikroregionu Jesenicko, Sdružení měst a obcí Jesenicka a Euroregionu Praděd.
</p>

