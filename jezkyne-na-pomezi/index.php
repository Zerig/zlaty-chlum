<h2>Co je v jeskyních k vidění?</h2>
<p>
	Jeskyně Na Pomezí byly objeveny v roce 1936 a zpřístupněny veřejnosti v roce 1950. Turisté mohou obdivovat úseky jeskyní o délce 410 metrů, v kterých vznikla bohatá krápníková výzdoba, sintrové polevy, kulovité útvary zvané jeskynní perly a podzemní jezírka. Tyto jeskyně jsou největšími v Jesenickém krasu. Chodby se rozšiřují do prostorných dómů, z nichž nejzajímavější je dóm Zvonice, který při poklepu zní jako zvonkohra.
