<h2>Lokalita jeskyní</h2>
<p>
	Jeskyně Na Pomezí se nacházejí ve stejnojmenném sedle v Rychlebských horách a pouhé 2 kilometry od Lipové-lázně. Do jeskyní se vstupuje uměle proražených vchodem u silnice do Vápenné. Prohlídka jeskyní trvá přibližně 45 minut. Návštěvníci si mohou u prostorného parkoviště zakoupit suvenýry a občerstvit se v bufetu.
</p>

<h2>Kudy k jeskyním?</h2>
<p>
	K jeskyním Na Pomezí se turisté dostanou automobilem z Jeseníku po silnici I/60 přes Lipovou-lázně. Jeskyně se nalézají asi 200 metrů pod vrcholem Pomezí směrem na Vápennou. U jeskyní nechybí záchytné parkoviště. Turisté rovněž mohou využít vlakovou nebo autobusovou dopravu a vystoupit na zastávce Lipová-lázně, jeskyně. Výstupní zastávky jsou od jeskyní vzdáleny 200 metrů a turisty do cíle dovede modrá a zelená značka.
</p>

