<h1>Jeskyně Na Špičáku</h1>



<h2>Jeskyně Na Špičáku u obce Písečná</h2>
<p>
Jeskyně Na Špičáku se nachází v podzemí kopce Špičák (516 m n. m.), který stojí 1 kilometr od Písečné. Krásy jeskyně mohou turisté obdivovat v rámci prohlídkové trasy o délce 220 metrů. Chodby se srdčitým až vejčitým tvarem získaly svou podobu působením podzemních jezer a prouděným vod tajícího pevninského ledovce. 
</p>

<h2>Obdivuhodné stáří jeskyně</h2>
<p>
V jeskyni se nalézají mořské usazeniny staré 350 až 380 milionů let, nazývané „devonský mramor“. Chodby se na mnoha místech rozšiřují do dómů s již poničenou krápníkovou výzdobou. Jeskyni Na Špičáku mohou v současné době navštívit také invalidní lidé, neboť prohlídková trasa je zcela bezbariérová.
</p>

<h2>Historie jeskyně datovaná od 15. století</h2>
<p>
První písemná zmínka o jeskyni Na Špičáku pochází z roku 1430 od Anthoniuse Waleho. V předchozích staletích sloužila jeskyně jako úkryt, čehož jsou důkazem epigrafické nápisy z roku 1519. V letech 1884 až 1885 otevřel jeskyni pro veřejnost Sudetoněmecký horský spolek. V dnešní podobě je jeskyně zpřístupněna od roku 1955. V roce 2007 se dočkala rozsáhlé rekonstrukce.
</p>

<h2>Kudy do jeskyně?</h2>
<p>
	K jeskyni Na Špičáku se turisté dostanou pohodlně autem z Jeseníku po silnici I/44 přes Českou Ves, směr Mikulovice. Trasa vede do Písečné, odkud návštěvníci musejí odbočit doleva na I/455 směr Supíkovice. Asi 1 kilometr za Písečnou se sjíždí z komunikace směrem k jeskyním. Turisté se mohou vydat na místo také vlakem nebo autobusem. Výstupní zastávky jsou od jeskyní zhruba 1 kilometr daleko.
</p>

<h2>Národní přírodní památka Na Špičáku</h2>
<p>
	Jeskyně a její povrchové a podzemní krasové jevy patří pod ochranu Národní přírodní památky Na Špičáku, která zaujímá rozlohu 7 hektarů. Rozkládá se mezi obcemi Písečná a Supíkovice. Charakteristický je pro ni buk lesní, tis červený, borovice lesní, modřín opadavý, javor klen a jalovec obecný. V jeskyni zimují letouni vrápenec malý a netopýři.
</p>
