<strong>Otorinolaryngologie</strong> (nebo též otorhinolaryngologie či otorinolaryngo- logie) je známá pod
zkratkou <strong>ORL</strong> nebo také "ušní, nosní, krční". Název vychází z řeckého otos (druhý pád slova ucho),
rhinos (nos), larynx (hrtan) a logos (věda). V překladu jde tedy o vědu o uchu, nosu a krku.