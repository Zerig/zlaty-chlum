<h1>Město Jeseník</h1>



<h2>Kde se město nachází?</h2>
<p>
	Město Jeseník leží v oblasti České vysočiny a nachází se na rozhraní Rychlebských hor, Hrubého Jeseníku a Zlatohorské vrchoviny. Sousedí s obcemi Česká Ves, Mikulovice, Lipová-lázně, Bělá pod Pradědem a Zlatá Hora. Krajské město Olomouc je od Jeseníku vzdáleno vzdušnou čarou 69,5 kilometru. Jeseníkem protéká řeka Bělá, do které ústí městský potok Staříč.
</p>

<h2>Historie v kostce</h2>
<h3><i>Počátek ve 13. století</i></h3>
<p>
První dochovaná zmínka o Jeseníku pochází z roku 1267 a v latinském pramenu je o něm hovořeno jako o místu zbaveného lesa. Oblast měla strategickou polohu na cestě z Moravy do Slezska a do konce 13. století si Jeseník vysloužil status města. V této době byl postaven frývaldovský hrad, který mohou turisté obdivovat i v současnosti.
</p>

<h3><i>Rozkvět města a temná stránka dějin</i></h3>
<p>
	Město zažilo svůj rozkvět díky nalezišti železné rudy v okolí. V 17. století postihly oblast čarodějnické procesy, které si vyžádaly 100 obětí. Nastalo období útlumu a město se začalo znovu rozvíjet až na počátku 19. století, za což vděčilo továrně na jemné prádlo a vodoléčebnému ústavu V. Priessnitze.
</p>

<h3><i>Novodobá historie města Jeseník</i></h3>
<p>
	Od roku 1918 se město stalo součástí Československa, ale po Mnichovské dohodě připadlo Německu. Po válce byli němečtí obyvatelé odsunuti a město Frývaldov bylo přejmenováno na Jeseník. Po roce 1989 se oblast Jesenicka zaměřila na rozvoj cestovního ruchu.
</p>

<h2>Zajímavosti města Jeseník</h2>
<p class="list-paragraph">
	Město Jeseník je ve všech ohledech zajímavým místem. Na co všechno se mohou turisté těšit?
</p>
<ul class="article-list">
<li>V roce 2017 město Jeseník oslavilo 750 let od svého vzniku.</li>
<li>V domu Katovna vzniklo česko-polské informační centrum. Ve skutečnosti v této budově nikdy žádní kati nežili.</li>
<li>Vývoj města ovlivnil Vincenz Priessnitz, který má v městském parku Smetanovy sady pomník.</li>
<li>Ve městě a jeho okolí je řada sportovišť, například lázeňské kluziště, golf Jeseník nebo tenisové kurty.</li>
<li>V Jeseníku naleznou turisté nové zrekonstruované koupaliště.</li>
<li>Kulturní život se odehrává v divadle Petra Bezruče, kde se pořádají hudební, taneční a divadelní představení.</li>
</ul>

<h2>Památky města Jeseník</h2>
<p>
	V Jeseníku mohou turisté obdivovat několik církevních památek z různých období. Za zmínku stojí farní kostel Nanebevzetí Panny Marie s budovou „Biskupství ostravské – opavské děkanství Jeseník“, kostel Českobratrské církve evangelické nebo kostel sv. Jiří. Častým výletním cílem je Vodní tvrz, v níž sídlí Vlastivědné muzeum Jesenicka. Co se týče přírodních zajímavostí, turisté mohou navštívit Čertovy kameny, jeskyně, Velké mechové jezírko nebo lokalitu Rejvíz.
</p>

<h2>Priessnitzovy lázně</h2>
<p>
	Priessnitzovy lázně proslavil Vincenz Priessnitz, po jehož vzoru vznikly vodoléčebné ústavy po celé Evropě. Po jeho smrti pokračoval v jeho práci doktor Josef Schindler. Priessnitzovy metody byly obohaceny o tehdejší vědecké poznatky moderní medicíny. V současné době pomáhá všem lázeňským pacientům Balneopark, v rámci kterého se využívá Priessnitzova přírodní lázeň horních a dolních končetin.
</p>
<p>
	Na základě dochovaných archivních materiálů se v lázních aplikuje Priessnitzova metoda, která zlepšuje prokrvení dolních končetin, hlavy, krku i horní poloviny těla. Procedura probíhá tak, že pacienti chodí čapí chůzí v bazénku s přírodní protékající chladnou vodou nebo noří horní končetiny do mělkého bazénku s chladnou vodou o teplotě 8 až 15 stupňů Celsia.
</p>
<h2>Tip, co ve volném čase</h2>
<p>
		Další pýchou města Jeseník je Minipivovar Jeseník Lichtwitz, který stojí v ulici Otakara Březiny. Pivovar má mladou historii, která se datuje k roku 2011. Rodinný pivovar vede sládek Stanislav Orel a vyrábí se v něm nefiltrovaná a nepasterovaná piva. Hosté si mohou dát v pivnici piva od 10 do 16 % extraktu původní mladiny.
</p>
