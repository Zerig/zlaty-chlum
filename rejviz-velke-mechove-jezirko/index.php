<h2>Kudy k jezírku?</h2>
<p class="list-paragraph">
K Velkému mechovému jezírku turisté dojdou po naučné stezce Rejvíz, která začíná ve Zlaté Hoře. Výchozím bodem naučné stezky je dřevěná chatka, kde si turisté musejí zakoupit vstupenku. Dále pokračují po povalovém chodníku až k jezírku. 
