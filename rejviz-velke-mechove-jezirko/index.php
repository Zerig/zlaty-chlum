<h2>Kde nalezneme Velké mechové jezírko?</h2>
<p>
Velké mechové jezírko je rašelinné jezero, které se nachází ve středu rašeliniště národní přírodní rezervace Rejvíz, v okrese města Jeseník a 1,5 km od osady Rejvíz. Vrstva rašeliniště má neuvěřitelné 3 metry. Jezírko je dlouhé 68,5 metrů a široké 41 metrů. Rozprostírá se v nadmořské výšce 769 metrů.
