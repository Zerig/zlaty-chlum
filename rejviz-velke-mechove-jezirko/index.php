<li>Pokud turisté zvolí cestu vlakem, musejí vystoupit v Jeseníku a poté se vydají po modré, zelené, červené nebo žluté turistické značce. Využít také mohou přepravu autobusem směr Rejvíz, kde vystoupí na zastávce Zlaté Hory, Rejvíz – zotavovna</li>
