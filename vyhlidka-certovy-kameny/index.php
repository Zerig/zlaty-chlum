<h1>Vyhlídka Čertovy kameny</h1>



<h2>Výhled na širé okolí</h2>
<p>
Vyhlídka Čertovy kameny se nachází na stejnojmenném skalním útvaru, který je vysoký 40 metrů a dlouhý 100 metrů. V těsné blízkosti stojí turistická chata s restaurací a ubytováním, jež byla postavena na počátku 20. století. Z vyhlídky se naskýtá krásný výhled na město Jeseník, Zlatohorsko, Rychlebské hory, údolí říčky Bělá a polské Otmuchovské jezero.
</p>

<h2>Na Čertovy kameny pěšky</h2>
<p>
Čertovy kameny se rozprostírají na severním úbočí Zlatého Chlumu, asi 3 kilometry od města Jeseník. Turisté se k nim dostanou pěšky i autem. Vyrazit mohou z Jeseníku po žluté značce od vlakového či autobusového nádraží nebo po modré značce od autobusového nádraží. Čertovy kameny jsou také přístupny po modré značce z České Vsi (vlaková zastávka), po žluté značce z Rejvízu či po červené značce přes rozhlednu Zlatý Chlum.
</p>

<h2>Na Čertovy kameny pěšky nebo na kole</h2>
<p>
Jestliže se turisté vydají do oblasti autem či na kole, musejí na silnici I/44 v Jeseníku projet kruhovým objezdem na Českou Ves, Mikulovice a Polsko a na další křižovatce odbočit doprava. Po 50 metrech cesty po hlavní silnici spatří po pravé straně směrovku na Čertovy kameny. Dále pokračují po úzké silnici až k chatě Čertovy kameny.
</p>

<h2>Skvělé podmínky pro horolezce</h2>
<p>
Čertovy kameny se staly oblíbeným místem horolezců. Skály jsou pro horolezce výzvou, protože patří do nejtěžší oblasti na Jesenicku. Horolezci mohou vyzkoušet své dovednosti na plotnách a kolmých a převislých stěnách. Pro jejich bezpečnost nechybí na skalách jistící kruhy a nýty. Vrcholy věží jsou opatřeny slaňovacími kruhy. Horolezci si mohou vybrat z 90 cest s různou obtížností III až IX.
</p>


<h2>Turistická chata Čertovy kameny</h2>
<p>
Od chaty Čertovy kameny v nadmořské výšce 694 metrů turisté dojdou k samotné vyhlídce. Chata je výborným zázemím pro strávení letní i zimní dovolené. Turisté mohou v oblasti poznávat krásy okolí, podniknout výlet na rozhlednu Zlatý Chlum nebo se věnovat turistice, cykloturistice a dalším aktivitám.
</p>
